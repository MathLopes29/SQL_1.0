CREATE TABLE EMPREGADOS(
MATR CHAR(6),
NOME VARCHAR(12),
SOBRENOME VARCHAR(30),
DEPT CHAR(30),
FONE VARCHAR(18),
DATA_ADM DATETIME,
CARGO CHAR(30),
SEXO CHAR(20),
IDADE INT,
SALARIO NUMERIC(10,2),
COMISSAO NUMERIC(10,2),
);

-- BUSCAS --
SELECT * FROM EMPREGADOS;
SELECT COUNT(*)  FROM EMPREGADOS ;
SELECT * FROM EMPREGADOS WHERE CARGO = CARGO;

-- BUSCA POR IDADE E POR LETRA --
SELECT * FROM EMPREGADOS WHERE IDADE between 30 and 40;
SELECT * FROM EMPREGADOS WHERE IDADE between 20 and 30;
SELECT * FROM EMPREGADOS WHERE NOME like '%J%';

-- SALÁRIO MAIOR QUE R$5000.00 --
SELECT * FROM EMPREGADOS WHERE SALARIO >= 5000.00;

-- BUSCA POR SETOR --
SELECT * FROM EMPREGADOS WHERE CARGO ='Programador';
SELECT * FROM EMPREGADOS WHERE DEPT ='TI';
SELECT * FROM EMPREGADOS WHERE DEPT ='Marketing';
SELECT * FROM EMPREGADOS WHERE DEPT ='Vendas';

-- MÉDIA DE SALÁRIO --
SELECT AVG(SALARIO) AS 'MEDIA' FROM EMPREGADOS;

-- MÉDIA DE IDADE --
SELECT AVG(IDADE) AS 'MEDIA' FROM EMPREGADOS;

-- SOMATÓRIO --
SELECT SUM(SALARIO)AS 'SOMATORIO' FROM EMPREGADOS;

-- MAX E MIN SALÁRIO --
SELECT MAX(SALARIO) AS 'MAXIMO' FROM EMPREGADOS;
SELECT MIN(SALARIO) AS 'MINIMO' FROM EMPREGADOS;

-- DELETAR TABELA --
DROP TABLE EMPREGADOS;

